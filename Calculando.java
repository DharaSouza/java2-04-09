package Calculos;

public class Calculando {

	int num1= 3, num2= 5;
	
	public double somar() {
	System.out.println (num1+num2);
	return 0;
	}
	
	public double sub() {
	System.out.println (num1-num2);
	return 0;
	}
	
	public double mult() {
	System.out.println (num1*num2);
	return 0;
	}
	
	public int div() {
	System.out.println (num1/num2);
	return 0;
	}
	public int exp() {
	System.out.println (Math.pow(num1 ,num2));
	return 0;
	
	
	}



}
